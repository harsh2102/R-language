#conditional operator and logical operator

var1<-25
var2<-15

if((var1 + var2)>100){
  print('the sum is greater than 100')
}else if((var1 + var2)>65){
  print('the sum is greater than 65')
}else if((var1 + var2)>55){
  print('the sum is greater than 55')
}else if((var1 + var2)>45){
  print('the sum is greater than 45')
}else{
  print('the sum is greater than & equal to 40')
}
